const updateCharacter = (str, index, newChar) => {
  return (str.substring(0, index) + newChar + str.substring(index + 1));
};

const cycle = (grid) => {
	let newGrid = [];
	for (let r = 0; r < grid.length; r++) {
  	let newRow = '';
    for (let c = 0; c < grid[r].length; c++) {
    	newRow += grid[r][c] === '#' ? '#' : '.';
    }
    newGrid.push(newRow);
  }
  
  for (let r = 0; r < grid.length; r++) {
  	let row = grid[r];
    for (let c = 0; c < row.length; c++) {
    	if (row[c] === 'S' || row[c] === 'O') {
      	// up
        if (r > 0) {
        	if (grid[r - 1][c] != '#') {
          	newGrid[r - 1] = updateCharacter(newGrid[r - 1], c, 'O');
          }
        }
        // right
        if (c < row.length - 1) {
        	if (grid[r][c + 1] != '#') {
          	newGrid[r] = updateCharacter(newGrid[r], c + 1, 'O');
          }
        }
        // down
        if (r < grid.length - 1) {
        	if (grid[r + 1][c] != '#') {
          	newGrid[r + 1] = updateCharacter(newGrid[r + 1], c, 'O');
          }
        }
        // left
        if (c > 0) {
        	if (grid[r][c - 1] != '#') {
          	newGrid[r] = updateCharacter(newGrid[r], c - 1, 'O');
          }
        }
      }
    }
  }
  
	return newGrid;
};

