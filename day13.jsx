const getHorizontal = (input) => {
  let cols = [];
  for (let c = 0; c < input[0].length; c++) {
  	let col = '';
    for (let row of input) {
    	col += row[c];
    }
    cols.push(col);
  }
  
  for (let c = 0; c < cols.length; c++) {
  	if (cols[c] === cols[c - 1]) {
    	let match = true;
      for (let i = 1; i <= 12; i++) {
        if (c > i && c < cols.length - i) {
          if (cols[c + i] !== cols[c - i - 1]) {
            match = false;
          	break;
          }
        } else {
          if (match) {
          	return c;
          }
        }
      }
      if (match) {
      	return c;
      }
    }
  }
  
  return 0;
};

const getVertical = (input) => {
  for (let r = 0; r < input.length; r++) {
  	if (input[r] === input[r - 1]) {
    	let match = true;
      for (let i = 1; i <= 12; i++) {
        if (r > i && r < input.length - i) {
          if (input[r + i] !== input[r - i - 1]) {
            match = false;
          	break;
          }
        } else {
          if (match) {
          	return r;
          }
        }
      }
      if (match) {
      	return r;
      }
    }
  }
  
 return 0;
};

